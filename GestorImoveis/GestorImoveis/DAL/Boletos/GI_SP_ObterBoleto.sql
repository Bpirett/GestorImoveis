CREATE PROCEDURE [dbo].[GI_SP_ObterBoleto]

@CODCONTR VARCHAR(10),
@CODBOL VARCHAR(10),
@NUMBOL VARCHAR(3)

AS
BEGIN
 SELECT 
  CODBOL
 ,NUMBOL
 ,NOMELOCADBOL
 ,NOMELOCATBOL
 ,VLRBOL
 ,VLRIPTUBOL
 ,DTPAGBOL
 ,ENDERBOL
 ,DTVCTBOL
 ,PAGBOL
 ,JRCOMBOL
 ,VLRCOMBOL
 ,DESCONJRBOL
 ,VLRDESCONTBOL
 ,JRMULBOL
 ,VLRMULBOL
 FROM CBOLETOS
 WHERE CODCONTRBOL = @CODCONTR AND CODBOL= @CODBOL AND  NUMBOL = @NUMBOL
END
GO

