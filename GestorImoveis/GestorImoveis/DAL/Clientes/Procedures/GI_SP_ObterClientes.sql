
CREATE PROCEDURE GI_SP_ObterClientes

@FILTRO VARCHAR(100),
@VALOR  VARCHAR(100)

AS

IF(@FILTRO = 'Codigo')
BEGIN
 SELECT 
CODCLI,
CPFCLI ,
NOMECLI ,
TELEFCLI ,
CELCLI ,
EMAILCLI, 
TIPOCLI ,
ATIVOCLI ,
CEPCLI ,
ENDCLI ,
NUMIMOVCLI ,
BAIRROCLI ,
CIDADECLI ,
UFCLI ,
COMPLCLI, 
COMPL2CLI
 FROM CCLIENTES
 WHERE CODCLI = @VALOR
END
ELSE IF(@FILTRO = 'Nome')
BEGIN
 SELECT 
CODCLI,
CPFCLI ,
NOMECLI ,
TELEFCLI ,
CELCLI ,
EMAILCLI, 
TIPOCLI ,
ATIVOCLI ,
CEPCLI ,
ENDCLI ,
NUMIMOVCLI ,
BAIRROCLI ,
CIDADECLI ,
UFCLI ,
COMPLCLI, 
COMPL2CLI
 FROM CCLIENTES
 WHERE NOMECLI = @VALOR
 END
ELSE IF(@FILTRO = 'CPF')
BEGIN
 SELECT 
CODCLI,
CPFCLI ,
NOMECLI ,
TELEFCLI ,
CELCLI ,
EMAILCLI, 
TIPOCLI ,
ATIVOCLI ,
CEPCLI ,
ENDCLI ,
NUMIMOVCLI ,
BAIRROCLI ,
CIDADECLI ,
UFCLI ,
COMPLCLI, 
COMPL2CLI
 FROM CCLIENTES
 WHERE CPFCLI = @VALOR
 END
ELSE
BEGIN
 SELECT 
CODCLI,
CPFCLI ,
NOMECLI ,
TELEFCLI ,
CELCLI ,
EMAILCLI, 
TIPOCLI ,
ATIVOCLI ,
CEPCLI ,
ENDCLI ,
NUMIMOVCLI ,
BAIRROCLI ,
CIDADECLI ,
UFCLI ,
COMPLCLI, 
COMPL2CLI
 FROM CCLIENTES
 END
GO

