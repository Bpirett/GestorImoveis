	  WHEN 'I' THEN 'INATIVO' END as ATIVOANUNC
 FROM CANUNCIOS
 WHERE TIPOIMOVANUNC = @VALOR
 END
 ELSE
BEGIN
 SELECT 
    CODANUNC
      ,ENDANUNC
