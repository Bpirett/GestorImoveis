
CREATE PROCEDURE GI_SP_RelListaAnuncio

@FILTRO VARCHAR(100),
@VALOR  VARCHAR(100)

AS

IF(@FILTRO = 'Codigo')
BEGIN
 SELECT 
   CODANUNC
