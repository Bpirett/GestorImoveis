      ,PLAYGROUDANUNC
      ,QUADRAANUNC
      ,SGINASTICAANUNC
      ,SFESTAANUNC
      ,SJOGOSNUNC
      ,CASE ATIVOANUNC WHEN 'A' THEN 'ATIVO'
	  WHEN 'I' THEN 'INATIVO' END as ATIVOANUNC
 FROM CANUNCIOS
 WHERE CODANUNC = @VALOR
END
